package transposition.matrix2;

public class TranspositionMatrix2 {
    public static void main(String[] args) {
    int matrix[][] = new int[4][4];
    for (int i = 0; i < 4; i++) {
        for (int j = 0; j < 4; j++) {
            matrix[i][j] = 0; // initialize each element with 0
            System.out.print(matrix[i][j] + " ");
        }
        System.out.println();
    }
}
}






